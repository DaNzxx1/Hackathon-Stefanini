package com.danilo.operadores;

public class OperadorRelacional {
	public static void main(String[] args) {
		
		// ==
		System.out.println(10 == 11);
		
		// !=
		System.out.println(10 != 20);
		
		// >
		System.out.println(10 > 20);
		
		// <
		System.out.println(10 < 20);
		
		// <=
		System.out.println(10 <= 10);
		
		// >=
		System.out.println(10 >= 10);
		
	}
}
