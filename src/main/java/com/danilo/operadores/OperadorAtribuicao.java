package com.danilo.operadores;

public class OperadorAtribuicao {
	
	public static void main(String[] args) {
		// =
		int numero = 10;
		
		// Aditivo
		numero += 10;
		numero = numero + 10;
		
		// Subtrativo
		numero -= 10;
		numero = numero - 10;
		
		// Multiplicativo
		numero *= 10;
		numero = numero * 10;
		
		// Divisivo
		numero /= 10;
		numero = numero / 10;
		
		// Modular
		numero %= 2; //Pega o resto da divisï¿½o
	}
	
}
